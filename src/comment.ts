  dockerfile: (line) => `# ${line}`,
  erlang: (line) => `% ${line}`,
  git: (line) => `# ${line}`,
  go: (line) => `// ${line}`,
  graphql: (line) => `# ${line}`,
  groovy: (line) => `// ${line}`,
  html: (line) => `<!-- ${line} -->`,
  http: (line) => line,
  java: (line) => `// ${line}`,
